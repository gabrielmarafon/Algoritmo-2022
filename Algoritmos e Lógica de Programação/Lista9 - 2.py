primeira_nota = input("Qual é a primeira nota")
segunda_nota = input("Qual é a segunda nota")
terceira_nota = input("Qual é a terceira nota")

valorum = float(primeira_nota) * 2
valordois = float(segunda_nota) * 4
valortreis = float(terceira_nota) * 6

soma_das_notas = (valorum + valordois + valortreis)

media = (soma_das_notas) / 12

print(media)